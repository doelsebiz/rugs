Change of Mind
Items purchased can be returned within 3 days of receiving them if you have a change of heart. However, refunds or exchanges due to a change of mind will incur a 19% restocking fee and a non-refundable shipping fee of $39.99 (the initial delivery cost). Return shipping via courier is the customer's responsibility, though we can offer assistance by obtaining quotes from various third-party couriers as a courtesy, if available.

Returned products must be unused and packaged in their original containers. If products are used, soiled, or damaged, they may be rejected, or additional restocking charges may apply.

If you return a rug and wish to purchase another within 10 days, that's fine. However, we'll only accept returns for up to two rugs within this period, regardless of the number initially ordered. After placing a new order, you must wait 10 days to be eligible for additional returns. For example, if Bob buys 3 rugs, returns 1, and then purchases 2 more within 10 days, they can only return 1 more rug.

